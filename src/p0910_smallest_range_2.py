"""Problem 910: Smallest range II.

Given a list of numbers and a value k, where each number can only be modified to num-k or num+k, find the smallest range.

Constraints:
    0 <= len(nums) <= 10,000
    0 <= num <= 10,000
    0 <= k <= 10,000

Insights
- If k >= initial range of numbers, the best we could do is the initial range.
- This problem cannot be solved by simple iteration e.g. deciding on -k or +k one number
  at a time leads to sub-optimal solution. Same for iterating from left and right.
- Constraints on the size input are key here.

Solutions
- Exponential: trying each combination -/+k for every number. That is 2^10,000 (~ 10^3000) combinations to check
- Sliding window
    Generate -/+k offsets for all numbers
    Sort the result
    Use a sliding window algorithm to find smallest window that covers one varations of every number

"""

from typing import List, Tuple, Set, Optional


def smallest_range_2(nums: List[int], k: int) -> int:
    initial_min, initial_max = min(nums), max(nums)
    initial_range = initial_max - initial_min
    if k >= initial_range:
        return initial_range
    # Remove duplicates
    unique_nums = set(nums)
    offset_num_tuples = _generate_offset_num_tuples(
        unique_nums, k, initial_min, initial_max
    )
    offset_num_tuples.sort(key=lambda t: t[1])
    smallest_range = initial_range
    right_index = len(unique_nums) - 1
    for left_index, _ in enumerate(offset_num_tuples):
        right_index, window_range = _smallest_leftbound_window_range(
            offset_num_tuples, unique_nums, left_index, right_index
        )
        if window_range is None:
            break
        elif window_range < smallest_range:
            smallest_range = window_range
    return smallest_range


def _smallest_leftbound_window_range(
    offset_num_tuples: List[Tuple[int, int]],
    nums: Set[int],
    left_index: int,
    right_index: int,
) -> Tuple[Optional[int], Optional[int]]:
    index = left_index
    nums_copy = nums.copy()
    while nums_copy and index < len(offset_num_tuples):
        if offset_num_tuples[index][0] in nums_copy:
            nums_copy.remove(offset_num_tuples[index][0])
        index += 1
    if not nums_copy:
        right_index = index - 1
        smallest_leftbound_range = (
            offset_num_tuples[right_index][1] - offset_num_tuples[left_index][1]
        )
        return right_index, smallest_leftbound_range
    return None, None


def _generate_offset_num_tuples(
    nums: Set[int], k: int, initial_min: int, initial_max: int
) -> List[Tuple[int, int]]:
    offsets = []
    for num in nums:
        if num == initial_min:
            offsets.append((num, num + k))
        elif num == initial_max:
            offsets.append((num, num - k))
        else:
            offsets.extend([(num, num + k), (num, num - k)])
    return offsets


